        -- Update sequence
        EXECUTE format('SELECT setval(pg_get_serial_sequence(''%I'', ''%I''), COALESCE((SELECT MAX(%I) FROM public.%I), 0) + 1, false)', t, pk, pk, t);
    END LOOP;
END $$;

